package com.example.vegeta;

public class ListIngredient {
	private String nombre;
	private String descripcion;
	private String imagen_ingrediente;

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	public String getImagenIngrediente() {
		return imagen_ingrediente;
	}

	public void setImagenIngrediente(String imagen_ingrediente) {
		this.imagen_ingrediente = imagen_ingrediente;
	}
}